#include <stdio.h>

/*
        Aula 038: Operadores relacionais
    Menor que (a < b)
    Maior que (a > b)
    menor ou igual (a <= b)
    Maior ou igual (a >= b)
    Igual (a == B)
    Diferente (a != b)
*/

int main(){

    int a = 10, b = 20;

    printf("\n\ta < b : Resultado: %d\n", a < b);
    printf("\n\ta > b : Resultado: %d\n", a > b);
    printf("\n\ta <= b : Resultado: %d\n", a <= b);
    printf("\n\ta >= b : Resultado: %d\n", a >= b);
    printf("\n\ta == b : Resultado: %d\n", a == b);
    printf("\n\ta != b : Resultado: %d\n", a != b);


    return 0;
}